package JavaBasics01;

public class Day12 {
	
	int a = 10;
	int b = 20;
	
	void sum ()
	{
		System.out.println(a+b);
	}

	void sum (int x, int y)
	{
		System.out.println(x+y);
	}
	
	void sum (int x, int y , int z)
	{
		System.out.println(x+y+z);
	}
	
	void sum (int x, double y)
	{
		System.out.println(x+y);
	}
	
	void sum (double y, int x)
	{
		System.out.println(x+y);
	}
}
